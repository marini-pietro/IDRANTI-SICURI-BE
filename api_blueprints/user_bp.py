class UserLogin(Resource):
    ENDPOINT_PATHS = [f"/{BP_NAME}/auth/login"]

    @limiter.limit(lambda: get_rate_limit("strict"))
    def post(self) -> Response:
        if not LOGIN_AVAILABLE_THROUGH_API:
            return create_response(
                {
                    "error": "login not available through API server. Please contact authentication service directly"
                },
                STATUS_CODES["forbidden"],
            )

        try:
            data = user_login_schema.load(request.get_json())
        except ValidationError as err:
            return create_response({"error": err.messages}, STATUS_CODES["bad_request"])

        scheme = "https" if IS_AUTH_SERVER_SSL else "http"
        auth_url = f"{scheme}://{AUTH_SERVER_HOST}:{AUTH_SERVER_PORT}/auth/{AUTH_API_VERSION}/login"
        try:
            response = requests_post(auth_url, json=data, timeout=10)
        except RequestException as ex:
            log(
                message=f"Authentication service unavailable: {str(ex)}",
                level="ERROR",
                message_id="AUTHSRVUNAVAIL",
                sd_tags={"endpoint": request.path, "method": request.method},
            )
            return create_response(
                {"error": "authentication service unavailable"},
                STATUS_CODES["service_unavailable"],
            )

        if response.status_code == STATUS_CODES["ok"]:
            return create_response(response.json(), STATUS_CODES["ok"])

        if response.status_code == STATUS_CODES["unauthorized"]:
            log(
                message=f"User login failed for {data.get('email')}",
                level="WARNING",
                message_id="LOGINFAIL",
                sd_tags={"endpoint": request.path, "method": request.method},
            )
            return create_response(
                {"error": "Invalid credentials"}, STATUS_CODES["unauthorized"]
            )

        return create_response(
            {"error": "authentication service error"}, response.status_code
        )

    @jwt_validation_required()
    def options(self) -> Response:
        return handle_options_request(resource_class=self)


api.add_resource(UserResource, *UserResource.ENDPOINT_PATHS)
