class UserSchema(ma.Schema):
    """
    Marshmallow schema for validating and deserializing input data for User resources.
    Contains validation rules for each field and specifies that unknown fields in the input should be rejected with an error.
    The ID field is not included since it's auto-generated by the database and should not be provided in input data, providing it in the input will result in a validation error.
    This approach also provides protection against overposting/mass assignment vulnerabilities, ensuring that clients cannot set fields that they are not supposed to.
    """

    class Meta:
        unknown = RAISE  # additional fields in the input will be rejected with an error

    # no field for user_id since it's auto-generated by the database and should not be provided in input data
    email = fields.Email(required=True)
    first_name = fields.String(required=True, validate=safe_string)
    last_name = fields.String(required=True, validate=safe_string)
    password = fields.String(required=True)
    must_change_password = fields.Boolean(load_default=True)


user_schema = UserSchema()


def hash_password(password: str) -> str:
    salt = os_urandom(16)
    kdf = PBKDF2HMAC(
        algorithm=PBKDF2HMAC_SETTINGS["algorithm"],
        length=PBKDF2HMAC_SETTINGS["length"],
        salt=salt,
        iterations=PBKDF2HMAC_SETTINGS["iterations"],
        backend=PBKDF2HMAC_SETTINGS["backend"],
    )
    hashed_password = base64_urlsafe_b64encode(kdf.derive(password.encode("utf-8")))
    return f"{base64_urlsafe_b64encode(salt).decode('utf-8')}:{hashed_password.decode('utf-8')}"


class UserResource(Resource):
    ENDPOINT_PATHS = [f"/{BP_NAME}/<string:email>"]

    @jwt_validation_required()
    @limiter.limit(lambda: get_rate_limit("default"))
    def get(self, email, identity) -> Response:
        user = User.query.filter_by(email=email).first()
        if user is None:
            return create_response(
                {"error": "user not found"}, STATUS_CODES["not_found"]
            )

        log(
            message=f"User {identity} fetched user {email} data",
            level="INFO",
            message_id="USRGET",
            sd_tags={
                "endpoint": request.path,
                "method": request.method,
                "requester": identity,
                "target_user": email,
            },
        )
        return create_response(user.to_dict(), STATUS_CODES["ok"])

    @jwt_validation_required()
    @limiter.limit(lambda: get_rate_limit("default"))
    def patch(self, email, identity) -> Response:
        try:
            data = user_schema.load(request.get_json(), partial=True)
        except ValidationError as err:
            return create_response({"error": err.messages}, STATUS_CODES["bad_request"])

        user = User.query.filter_by(email=email).first()
        if user is None:
            return create_response(
                {"error": "user not found"}, STATUS_CODES["not_found"]
            )

        if "password" in data:
            user.password = hash_password(data["password"])
        if "first_name" in data:
            user.first_name = data["first_name"]
        if "last_name" in data:
            user.last_name = data["last_name"]
        if "must_change_password" in data:
            user.must_change_password = data["must_change_password"]

        db.session.commit()
        log(
            message=f"User {identity} updated user {email}",
            level="INFO",
            message_id="USRPATCH",
            sd_tags={
                "endpoint": request.path,
                "method": request.method,
                "updater": identity,
                "updated_user": email,
            },
        )
        return create_response({"success": f"User {email} updated"}, STATUS_CODES["ok"])

    @jwt_validation_required()
    @limiter.limit(lambda: get_rate_limit("default"))
    def delete(self, email, identity) -> Response:
        user = User.query.filter_by(email=email).first()
        if user is None:
            return create_response(
                {"error": "user not found with provided email"},
                STATUS_CODES["not_found"],
            )

        db.session.delete(user)
        db.session.commit()
        log(
            message=f"User {email} deleted user {identity}",
            level="INFO",
            message_id="USRDEL",
            sd_tags={
                "endpoint": request.path,
                "method": request.method,
                "deleter": email,
                "deleted_user": identity,
            },
        )
        return create_response({"success": f"User {email} deleted"}, STATUS_CODES["ok"])

    @jwt_validation_required()
    def options(self) -> Response:
        return handle_options_request(resource_class=self)


class UserPostResource(Resource):
    ENDPOINT_PATHS = [f"/{BP_NAME}"]

    @jwt_validation_required()
    @limiter.limit(lambda: get_rate_limit("default"))
    def post(self, identity) -> Response:
        try:
            data = user_schema.load(request.get_json())
        except ValidationError as err:
            return create_response({"error": err.messages}, STATUS_CODES["bad_request"])

        if User.query.filter_by(email=data["email"]).count() > 0:
            return create_response(
                {"error": "user with provided email already exists"},
                STATUS_CODES["conflict"],
            )

        new_user = User(
            email=data["email"],
            first_name=data["first_name"],
            last_name=data["last_name"],
            password=hash_password(data["password"]),
            must_change_password=data.get("must_change_password", True),
        )
        db.session.add(new_user)
        db.session.commit()

        log(
            message=f"User {identity} created user {data['email']}",
            level="INFO",
            message_id="USRPOST",
            sd_tags={
                "endpoint": request.path,
                "method": request.method,
                "identity": identity,
                "email": data["email"],
            },
        )
        return create_response(
            {
                "outcome": f"User {data['email']} created",
                "location": get_hateos_location_string(
                    bp_name=BP_NAME, id_=data["email"]
                ),
            },
            STATUS_CODES["created"],
        )

    @jwt_validation_required()
    def options(self) -> Response:
        return handle_options_request(resource_class=self)


